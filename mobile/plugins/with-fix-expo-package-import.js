const { withAppBuildGradle } = require('@expo/config-plugins');

const PATCH_MARKER = '// @generated by with-fix-expo-package-import';

const GRADLE_PATCH = `
${PATCH_MARKER}
tasks.withType(org.gradle.api.tasks.compile.JavaCompile).configureEach { compileTask ->
    if (compileTask.name == "compileDebugJavaWithJavac" || compileTask.name == "compileReleaseJavaWithJavac") {
        compileTask.doFirst {
            def packageListFile = file("$buildDir/generated/autolinking/src/main/java/com/facebook/react/PackageList.java")
            if (packageListFile.exists()) {
                def packageListText = packageListFile.getText("UTF-8")
                def fixedPackageListText = packageListText.replace("import expo.core.ExpoModulesPackage;", "import expo.modules.ExpoModulesPackage;")
                if (fixedPackageListText != packageListText) {
                    packageListFile.write(fixedPackageListText, "UTF-8")
                    logger.lifecycle("Patched ExpoModulesPackage import in generated PackageList.java")
                }
            }
        }
    }
}
`;

module.exports = function withFixExpoPackageImport(config) {
  return withAppBuildGradle(config, (modConfig) => {
    if (!modConfig.modResults.contents.includes(PATCH_MARKER)) {
      modConfig.modResults.contents += GRADLE_PATCH;
    }
    return modConfig;
  });
};
